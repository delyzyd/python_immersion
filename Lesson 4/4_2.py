# Напишите функцию принимающую на вход только ключевые параметры и возвращающую словарь,
# где ключ — значение переданного аргумента, а значение — имя аргумента.
# Если ключ не хешируем, используйте его строковое представление.

def reverse_kwargs(**kwargs):
    result = {}
    # Перебор ключевых аргументов
    for key, value in kwargs.items():
        try:
            # Попытка использовать значение как ключ
            result[value] = key
        except TypeError:
            # Если значение не хешируемое, используем его строковое представление
            result[str(value)] = key
    return result

# Пример использования
if __name__ == "__main__":
    # Создаем словарь с хешируемыми и нехешируемыми значениями
    result = reverse_kwargs(a=1, b="two", c=[1,2,3], d=(4,5))
    print(result)  